package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {

        System.out.println("Penjumlahan");
        int hasilPenjumlahan = 5 + 8;
        System.out.println("5 + 8 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Pengurangan");
        int hasilPengurangan = 60 - 8;
        System.out.println("60 - 8 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Perkalian");
        int hasilPerkalian = 6 * 9;
        System.out.println("6*9 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Pembagian");
        int hasilPembagian = 90/10;
        System.out.println("90/10 = " + hasilPembagian);
        System.out.println();

        System.out.println("Modulus");
        int hasilModulus = 80%7;
        System.out.println("80%7 = " + hasilModulus);
    }
}
